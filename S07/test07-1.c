/******************************************
  配列
    配列に1から10までの数字を代入して表示する。        
******************************************/
#include <stdio.h>
int main( void) {
  int a[10];
  for( int i = 0; i < 10; i++) {
    a[i] = i + 1;
  }
  for( int i = 0; i < 10; i++) {
    printf("%2d ", a[i]);
  }
  printf("\n");
  return 0;
}